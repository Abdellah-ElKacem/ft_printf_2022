#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		ft_putstr("(null)");
		i = 6;
	}
	else
	{
		while (s[i])
			write(1, &s[i++], 1);
	}
	return (i);
}
